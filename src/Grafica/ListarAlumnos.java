    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        //Mostrar los alumnos agregados a el ArrayList haciendo una copia
        //de la lista para no perder el encapsulamiento
        Iterator<Alumno> iterador = Sistema.getInstance().listadoAlumno();
        ArrayList<Alumno> aux = new ArrayList<>();
        while(iterador.hasNext()){
            Alumno alumno = iterador.next();
            aux.add(alumno);
            jlistAlumnos.setListData(aux.toArray());
            
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

